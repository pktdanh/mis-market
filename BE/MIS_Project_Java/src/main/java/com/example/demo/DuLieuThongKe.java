package com.example.demo;

public class DuLieuThongKe {
	public String thang;
	public String nam;
	public String account_CH;
	
	public DuLieuThongKe()
	{
		thang = "";
		nam = "";
		account_CH = "";
	}
	
	public DuLieuThongKe(String Thang, String Nam, String Account_CH)
	{
		this.thang = Thang;
		this.nam = Nam;
		this.account_CH = Account_CH;
	}

}
